#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = str;
	while (i < n)
	{
		tmp[i++] = c;
	}
	return (tmp);
}
